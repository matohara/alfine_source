//*****************************************************************************
// クラス設計
//*****************************************************************************
//----回転--------
void CBackTexture::SetBGTStatus(float fX, float fY, D3DXVECTOR3 pos, D3DXVECTOR3 rot)
{
	Size = { fX, fY };
	Position = pos;
	Rotation = rot;

	MakeVertex();
}

//*****************************************************************************
// プロトタイプ宣言
//*****************************************************************************


//*****************************************************************************
// グローバル変数
//*****************************************************************************
CBackTexture BackGroundTexture;

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitBG(void)
{
	// テクスチャ読み込み
	BackGroundTexture.LoadTexture(TEXTURE_GAME_BG);

	// 情報をセット
	BackGroundTexture.SetBGTStatus(700.0f, 400.0f, D3DXVECTOR3(0.0f, 0.0f, 400.0f), D3DXVECTOR3(D3DXToRadian(10), 0.0f, 0.0f));

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitBG(void)
{
	BackGroundTexture.ReleaseBuffer();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateBG(void)
{

}

//=============================================================================
// 描画処理
//=============================================================================
void DrawBG(void)
{
	BackGroundTexture.Draw();
}

